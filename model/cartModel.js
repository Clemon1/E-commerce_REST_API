const mongoose = require("mongoose");

const cartModel = new mongoose.Schema(
  {
    userId: {
      type: String,
      require: true,
    },
    products: [
      {
        productId: {
          type: String,
        },
        quantity: {
          type: Number,
          default: 1,
        },
      },
    ],
  },
  {
    timestamps: true,
  },
);

const cart = mongoose.model("cart", cartModel);

module.exports = cart;
